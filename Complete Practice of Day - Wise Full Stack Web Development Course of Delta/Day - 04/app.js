// Here we have to discuss about the switch case statements in JavaScript

let a = 9;

switch (a) {
  case 1:
    console.log("one");
    break;
  case 2:
    console.log("two");
    break;
  case 3:
    console.log("three");
    break;
  case 4:
    console.log("four");
    break;
  case 5:
    console.log("five");
    break;
  default:
    console.log("I am default case ");
}
