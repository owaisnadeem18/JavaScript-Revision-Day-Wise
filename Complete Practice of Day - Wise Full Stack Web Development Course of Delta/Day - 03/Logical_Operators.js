// OR , AND & NOT Operator

// AND Operator , will return true , if both of the statements are ture

// OR Operator , will return false , if both of the statements are false , all else conditions will return true

// NOT Operator , will return true , if both of the statements are ture

// AND Operator

marks = 85;

console.log(marks < 90 && marks > 80); // return true because both of the statements were true

// OR Operator

console.log(marks < 60 && marks > 90); // return false because both of the statements were true

// NOT Oeprator

console.log(!true); // will return false , because it reverts true to false and false to true
