let your_percentage = prompt("Please Enter your percentage = ");

if (your_percentage >= 80) {
  console.log("A+");
} else if (your_percentage >= 70) {
  console.log("B");
} else if (your_percentage >= 60) {
  console.log("B");
} else if (your_percentage >= 50) {
  console.log("C");
} else if (your_percentage < 50) {
  console.log("You are <b>Fail</b>");
} else {
  console.log("Please Enter a valid percentage !!! ");
}
