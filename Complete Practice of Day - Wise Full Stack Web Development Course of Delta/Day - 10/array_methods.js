let arr = ["Toyota", "Mercidies", "Mehran", "Suzuki"];

console.log(arr);
// Array methods:

// 1. Push

// Add some thing at the end of our array

arr.push("honda");
console.log(arr); // some thing has been added in my array at the end

// 2. Pop

// Remove some thing from the last index of our array

arr.pop();
console.log(arr); // some thing has been added in my array at the end
console.log(arr.pop());
console.log(arr); // some thing has been added in my array at the end

// 3. Shift

let array = ["Owais ", 843, true, "hello", false];

console.log(array.shift());
console.log(array); // If we want to remove element from the starting index of our array , then we use shift

// 4. Unshift()

// It adds a new item in the start of our array

array.unshift("Unshift ");

console.log(array);
